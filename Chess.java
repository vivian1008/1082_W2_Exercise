public class Chess{
    private String name, color, status, quantity, texture;

    public void setName(String setName){
        this.name = setName;
    }

    public String getName(){
        return name;
    }

    public void setColor(String setColor){
        this.color = setColor;
    }

    public String getColor(){
        return color;
    }

    public void setStatus(String setStatus){
        this.status = setStatus;
    }

    public String getStatus(){
        return status;
    }

    public void setQuantity(String setQuantity){
        this.quantity = setQuantity;
    }

    public String getQuantity(){
        return quantity;
    }

    public void setTexture(String setTexture){
        this.texture = setTexture;
    }

    public String getTexture(){
        return texture;
    }


}