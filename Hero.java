public class Hero{
    private String name, power, gender, ethnicity, type;

    public void setName(String setName){
        this.name = setName;
    }

    public String getName(){
        return name;
    }

    public void setPower(String setPower){
        this.power = setPower;
    }

    public String getPower(){
        return power;
    }

    public void setGender(String setGender){
        this.gender = setGender;
    }

    public String getGender(){
        return gender;
    }

    public void setEthnicity(String setEthnicity){
        this.ethnicity = setEthnicity;
    }

    public String getEthnicity(){
        return ethnicity;
    }

    public void setType(String setType){
        this.type = setType;
    }

    public String getType(){
        return type;
    }

    
}